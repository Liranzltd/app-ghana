<?php

namespace AppBundle\Entity;

/**
 * TierBenefit
 */
class TierBenefit
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $title;

    /**
     * @var \AppBundle\Entity\Tier
     */
    private $tier;


    /**
     * Get id.
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set title.
     *
     * @param string $title
     *
     * @return TierBenefit
     */
    public function setTitle($title)
    {
        $this->title = $title;

        return $this;
    }

    /**
     * Get title.
     *
     * @return string
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set tier.
     *
     * @param \AppBundle\Entity\Tier|null $tier
     *
     * @return TierBenefit
     */
    public function setTier(\AppBundle\Entity\Tier $tier = null)
    {
        $this->tier = $tier;

        return $this;
    }

    /**
     * Get tier.
     *
     * @return \AppBundle\Entity\Tier|null
     */
    public function getTier()
    {
        return $this->tier;
    }
}
